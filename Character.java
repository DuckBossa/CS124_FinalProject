import java.awt.geom.Rectangle2D;
public abstract class Character{
	public int atk;
	public int def;
	public int hp;
	public int vx,vy;
	public int x,y,w,h;
	public int lvl;
	public int face;
	public int seq;
	public Rectangle2D hitbox;

	public Character(int atk, int def, int vx, int vy, int x, int y,int w, int h, int lvl){
		this.atk = atk;
		this.def = def;
		this.vx = vx;
		this.vy = vy;
		this.x = x;
		this.y = y;
		this.w = w;
		this.h = h;
		this.lvl = lvl;
		hp = lvl*10;
		face = 1;
		seq = 1;
		hitbox = new Rectangle2D.Double(x,y,w,h);
	}

	public abstract void move(int dir);

	public boolean collide(Rectangle2D e){ // can be used for collision with arrows and rectangles generated by attacks
		if(hitbox.intersects(e))
			return true;
		else 
			return false;
	}

	public void updateRectangle(){
		hitbox.setRect(x,y,w,h);
	}

	public void levelUp(){
		hp = ++lvl * 10; 
	}

	public boolean takeDamage(int damage){
		if(damage - def <= 0)
			hp--;
		else{
			hp -= (damage - def);
		}
		return (hp <= 0);
	}
}